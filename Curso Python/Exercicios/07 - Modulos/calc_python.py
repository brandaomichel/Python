'''Modulo que contem duas funcoes uma para soma e outra para subtracao'''

def soma(n1, n2):
    '''Este funcao retorna a soma de dois numeros obtidos pela entrada'''
    return n1 + n2

def subtrai(n1, n2):
    '''Esta funcao retorna a subtraca de dois numeros obtidos pela entrada'''
    return n1 - n2
